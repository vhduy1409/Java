package sandwich;

public class conversion {
	
	
	public void convertStringToInt(String num)// convert String to Integer
	{
		int numConvert = Integer.parseInt(num);
		System.out.println(numConvert);
	}

	public void convertStringToDouble(String num)// convert String to Double
	{
		double numConvert = Double.parseDouble(num);
		System.out.println(numConvert);

	}
	public void convertStringToByte(String num)// Convert String to Byte
	{
		byte numConvert = Byte.parseByte(num);
		System.out.println(numConvert);

	}
	public void convertStringToLong(String num)// Convert String to Long
	{
		long numConvert = Long.parseLong(num);
		System.out.println(numConvert);
	}
	public void convertStringToShort(String num)// Convert String to short
	{
		short numConvert = Short.parseShort(num);
		System.out.println(numConvert);
	}
	public void convertStringToFloat(String num)// Convert String to float
	{
		float numConvert = Float.parseFloat(num);
		System.out.println(numConvert);
	}
	
	
	
}
